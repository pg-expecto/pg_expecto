#!/bin/bash

# Сравнение двух файлов в формате "ключ=значение"
# Игнорируются строки, начинающиеся с '#', а также комментарии после '#'
# Выводятся строки, где ключ присутствует в обоих файлах и значения отличаются,
# а также строки, присутствующие только в одном из файлов.
# Формат вывода: ключ | значение в 1м файле | значение во 2м файле
# Результат записывается в файл, указанный третьим параметром.
# После завершения работы скрипта для выходного файла устанавливаются права 777.

set -euo pipefail

# Проверка аргументов
if [ $# -lt 2 ]; then
    echo "Ошибка: укажите два входных файла." >&2
    echo "Использование: $0 файл1 файл2 [выходной_файл]" >&2
    exit 1
fi

file1="$1"
file2="$2"
output_file="${3:-}"

# Проверка существования файлов
if [ ! -f "$file1" ]; then
    echo "Ошибка: файл '$file1' не найден." >&2
    exit 1
fi
if [ ! -f "$file2" ]; then
    echo "Ошибка: файл '$file2' не найден." >&2
    exit 1
fi

# Функция вывода результата
output() {
    if [ -n "$output_file" ]; then
        echo "$1" >> "$output_file"
    else
        echo "$1"
    fi
}

# Очистка выходного файла, если он задан
if [ -n "$output_file" ]; then
    > "$output_file"
fi

# Вывод заголовка
output "ключ | $file1 | $file2"

# Ассоциативный массив для хранения значений второго файла
declare -A value2
# Массив для сохранения порядка ключей второго файла
keys2_order=()

# Чтение второго файла, заполнение value2 и keys2_order
while IFS= read -r line; do
    # Удаляем комментарий (всё после '#')
    line="${line%%#*}"
    # Пропускаем пустые строки
    [[ -z "$line" ]] && continue

    # Пропускаем строки без '='
    if [[ "$line" != *"="* ]]; then
        echo "Предупреждение: строка во втором файле не содержит '=', пропущена: $line" >&2
        continue
    fi
    key="${line%%=*}"
    val="${line#*=}"
    # Если ключ уже есть, перезаписываем (последнее вхождение), порядок не меняем
    if [[ -z "${value2[$key]+_}" ]]; then
        keys2_order+=("$key")
    fi
    value2["$key"]="$val"
done < "$file2"

# Чтение первого файла, сравнение
while IFS= read -r line; do
    # Удаляем комментарий (всё после '#')
    line="${line%%#*}"
    # Пропускаем пустые строки
    [[ -z "$line" ]] && continue

    # Пропускаем строки без '='
    if [[ "$line" != *"="* ]]; then
        echo "Предупреждение: строка в первом файле не содержит '=', пропущена: $line" >&2
        continue
    fi
    key="${line%%=*}"
    val1="${line#*=}"
    if [[ -n "${value2[$key]+_}" ]]; then
        # Ключ есть во втором файле
        val2="${value2[$key]}"
        if [[ "$val1" != "$val2" ]]; then
            output "$key | $val1 | $val2"
        fi
        # Удаляем ключ из массива, чтобы потом не выводить как отсутствующий во втором
        unset value2["$key"]
    else
        # Ключа нет во втором файле
        output "$key | $val1 | "
    fi
done < "$file1"

# Вывод ключей, оставшихся во втором файле (которых не было в первом)
for key in "${keys2_order[@]}"; do
    if [[ -n "${value2[$key]+_}" ]]; then
        output "$key | | ${value2[$key]}"
    fi
done

# Установка прав 777 на выходной файл, если он был указан
if [ -n "$output_file" ]; then
    chmod 777 "$output_file"
fi
